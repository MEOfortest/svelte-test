<h1>Из чего состоят фреймворки?</h1>

<p>Большинство фреймворков основано на шаблоне проектирования MVC (Model-View-Controller или модель-представление-контроллер).</p>

<p><b>Модель (Model)</b> — это место, где хранятся данные вашего приложения. Представим, что мы пришли в музей. Карта музея с описанием, где и в каком зале находится тот или иной экспонат, — это и есть модель.</p>

<p><b>Представление или отображение (View)</b> определяет взаимодействие с пользователем через модель. Он отвечает за отображение данных, определяет внешний вид проекта — веб-сайтов или приложений. От него зависят возможные действия пользователя: где печатать текст, какие кнопки нажимать.</p>

<p><b>Контроллер (Controller)</b> отвечает за реакцию двух предыдущих объектов на действия пользователя. Его код и логика проверяют, как сайт обрабатывает запросы и выдает ли правильный результат.</p>

