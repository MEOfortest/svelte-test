<h1>Что такое фреймворк?</h1>

<p>Создавая сайт, разработчики могут писать код с нуля сами: сделать скелет будущей страницы на HTML, стилизовать с помощью CSS. А могут воспользоваться наборами уже готового кода — библиотеками — для создания отдельных элементов сайта вроде блоков с иллюстрациями, текстами.</p>

<p>Но если их задача создать что-то сложнее сайтов, например веб-приложение, как соцсеть ВКонтакте — понадобится фреймворк, так будет быстрее и удобнее.</p>

<p>Например, фреймворки для фронтенда задают каркас будущего приложения, который состоит из компонентов. Компонент — как кирпичик для строительства веб-сайтов. Это маленькая часть кода, которая, например, добавляет на сайт кнопку или поле для ввода текста. Вы можете комбинировать вместе множество таких компонентов, чтобы создать большое и сложное веб-приложение, как конструктор из деталей LEGO.</p>
    
<img src="https://imgur.com/3u7Kjlp.png" width="90%" alt="Картинка 2">

<h1>В чем разница между фреймворком и библиотекой?</h1>

<p>Фреймворк — это как большая библиотека. Она решает не одну проблему в разработке веб-приложения, а сразу несколько. Например, упрощает верстку и стилизацию с помощью компонентного подхода, упрощает управление данными внутри приложения, предлагает свой способ взаимодействия между клиентом и сервером — то есть решает вообще все основные проблемы. При этом, чтобы пользоваться фреймворком, можно не устанавливать отдельные библиотеки.</p>

