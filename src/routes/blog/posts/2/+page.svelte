<p>Во всех объектно-ориентированных языках программирования есть классы и объекты. Они считаются ключевыми понятиями в ООП (объектно-ориентированном программировании). Рассказываем, что такое классы и объекты Python и как их создать.</p>

<h1>Классы и объекты в Python: определение</h1>

<p>Классы и объекты в Python можно сравнить с описанием любимых питомцев. Так, у каждого питомца есть имя, возраст, порода и другие характеристики. Все эти атрибуты присущи классам в Python. То есть класс — это определенная сущность или набор атрибутов. Например, если у животных набор атрибутов включает кличку, возраст и породу, то у человека — имя, возраст, национальность, а у автомобиля — марку, модель, год производства. Объект — это воплощение класса.</p>

<p>Создавать классы и объекты в Python нужно поступательно. Сразу с помощью ключевого слова class создаем класс: задаем его название и атрибуты. После этого можно перейти к созданию объекта. Для этого нужно использовать следующий запрос:</p>
    
<p>[название объекта] = [название класса] (начальные значения атрибутов)</p>

<p>К атрибутам можно обратиться и получить их значения. Также их легко можно изменить как:</p>

<p>[название объекта].[свойство] = [значение]</p>

